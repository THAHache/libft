#include <string.h>

int	ft_strncmp(const char *s1, const char *s2)
{
	int i;

	i = 0;
	while (s1[i] != '\0' && s2[i] != '\0' && s1[i] != s2[i])
	{
		i++;
	}
	return (s1[i] - s2[i]);
}